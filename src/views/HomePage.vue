O Projeto AgenteAchaPet

O protótipo

Este sistema é um protótipo. Ou seja, seus dados não vão sair do seu navegador. Ele tem praticamente todas as funcionalidades necessárias para 'funcionar de verdade'

Ele tem três funcionalidades: formulário que convova uma busca; o cartaz de divulgação da busca; a listagem de buscas ativas.
Uma quarta funcionalidade está sendo desenvolvida: no cartaz vai ter uma caixa de texto para entrar em contato com a pessoa que ativou a busca, seja para apoiar ou comunicar, por exemplo, um possível avistamento.

Do Financiamento [coletivo]

O AgenteAchaPet é um projeto que visa ser comunitário. Isto é, almejamos que ele seja elaborado e realizado com a comunidade.
A princíío os bairros Vila Morse e Vila Sônia no múnicipio de São Paulo - SP.
O caráter comunitário do projeto significa que o financiamento também deve ser comunitário. Neste sentido, o AgenteAchaPet vai romper a barreira
do seu navegador se e somente se houver contribuição suficiente para que ele seja de fato 'colocado no ar', como dizem.
O mesmo vale para algumas outras funcionalidades. Funcionalidades precisam ser implementadas.
A implementação delas é grátis, custa somente meu tempo de vida [kkkkkkkk]. Funcionalidades como a listagem, o cadastro, o recurso de enviar e-mail, etc.
No entanto, independentemente do meu tempo de vida, há funcionalidades que primeiro é preciso pagar para depois implementar.
Um exemplo disso, é o serviço que envia mensagens via celular. Este serviço é necessario para funcionalidades como 'botão do pânico', ou 'notificação de avistamento'.
Neste caso, além do emprego meu tempo de vida, seria preciso que a comunidade acrasse com os custos do serviço de envio de mensagem via celuar, para que aí seja possível implementar aquelas fuincionalidades.
